#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	n;

	if (nb == -2147483648)
	{
		write(1, "-2", 2);
		nb = 147483648;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	n = nb % 10 + 48;
	if (nb / 10 != 0)
		ft_putnbr(nb / 10);
	write(1, &n, 1);
}
