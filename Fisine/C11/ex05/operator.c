long long	plus(long long num1, long long num2)
{
	return (num1 + num2);
}

long long	minus(long long num1, long long num2)
{
	return (num1 - num2);
}

long long	multiple(long long num1, long long num2)
{
	return (num1 * num2);
}

long long	division(long long num1, long long num2)
{
	return (num1 / num2);
}

long long	modulo(long long num1, long long num2)
{
	return (num1 % num2);
}
